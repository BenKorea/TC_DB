my_clean_string_edges <- function(str) {
  
  # 정규식 설명:
  # ^\\s*          : 문자열의 시작과 0개 이상의 공백 문자
  # (?:#1)?        : '#1' 패턴이 0번 또는 1번 나타나는 부분 (캡처를 하지 않는 그룹)
  # [.,]?          : '.' 또는 ','가 0번 또는 1번 나타나는 부분
  # \\s*           : 다시 0개 이상의 공백 문자
  str <- sub("^\\s*(?:#1)?[.,]?\\s*", "", str)
  str <- gsub("\\s+$", "", str)
  
  return(str)
}
